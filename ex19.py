# Ler dois valores (considere que não serão lidos valores iguais) e escrever o maior deles.
print('=================== MAIORIDADE ENTRE OS VALORES ===================')
v1 = int(input('Digite um Valor: '))
v2 = int(input('Digite outro Valor: '))

if v1 > v2:
    print(f'{v1} é MAIOR!')
elif v2 > v1:
    print(f'{v2} é MAIOR!')

